class HuffmanCode
{

   private String character;
   private String code;
   
   /////////////////////////
   // Constructor
   /////////////////////////

   HuffmanCode(String character, String code){
      this.character = character;
      this.code = code;
   }


   /////////////////////////
   // Public Methods
   /////////////////////////
   
   public String getCharacter(){
      return this.character;
   }
   
   public String getCode(){
      return this.code;
   }
   
   
   /////////////////////////
   // Private Methods
   /////////////////////////      

}